function tabsForApp(app) {
  const modules = Array.isArray(app?.modules) ? app.modules : [];
  const legacyApp = modules.length === 0;
  const enabledFeatures = FEATURE_TABS.filter((tab) => legacyApp || tab.modules.some((module) => modules.includes(module)));
  return [CORE_TABS[0], ...enabledFeatures, CORE_TABS[1]];
}

function renderCitations(text) {
  // Replace [S1] etc with clickable citation badges
  const parts = text.split(/(\[S\d+\])/g);
  return parts.map((p, i) => {
    if (/^\[S\d+\]$/.test(p)) {
      return <span key={i} className="citation-badge" data-testid={`citation-${p}`}>{p}</span>;
    }
    return <span key={i}>{p}</span>;
  });
}

function mergeCitations(existing = [], incoming = []) {
  const merged = new Map();
  [...existing, ...incoming].filter(Boolean).forEach((citation) => {
    merged.set(citation.tag || citation.chunk_id || JSON.stringify(citation), citation);
  });
  return Array.from(merged.values());
}

function parseSseBlock(raw) {
  const lines = String(raw || "").split(/\r?\n/);
  const event = lines.find((line) => line.startsWith("event:"))?.slice(6).trim() || "message";
  const dataText = lines
    .filter((line) => line.startsWith("data:"))
    .map((line) => line.slice(5).trimStart())
    .join("\n");
  if (!dataText) return { event, data: null };
  return { event, data: JSON.parse(dataText) };
}

