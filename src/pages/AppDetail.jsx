function citationSources(run) {
  return (run?.citations || []).map((citation) => {
    const ord = Number(citation.ord);
    return {
      tag: citation.tag,
      title: citation.doc_name || citation.title || "Citation source",
      status: citation.chunk_id ? `Chunk ${Number.isFinite(ord) ? ord + 1 : citation.chunk_id}` : "Citation",
      score: citation.score,
      preview: citation.preview || "",
    };
  });
}

function documentSources(documents = [], includePreviews = true) {
  return documents.map((doc, index) => ({
    tag: `D${index + 1}`,
    title: doc.filename || doc.original_filename || `Document ${index + 1}`,
    status: [
      doc.text_extraction_status || "UNKNOWN",
      doc.chunk_count !== undefined ? `${doc.chunk_count} chunks` : "",
      doc.indexed_char_count ? `${doc.indexed_char_count} indexed chars` : "",
    ].filter(Boolean).join(" / "),
    preview: includePreviews ? (doc.text_preview || doc.text_extraction_message || "") : (doc.text_extraction_message || ""),
    chunk_count: doc.chunk_count,
  }));
}

function selectedAnswerExportPayload(app, run, documents = []) {
  const sources = citationSources(run);
  const fallbackSources = sources.length ? sources : documentSources(documents, false);
  return {
    title: `${app?.name || "ADA Studio"} - Selected Answer`,
    subtitle: run?.question || "Exported app answer",
    metadata: {
      App: app?.name || "Unknown app",
      Jurisdiction: app?.jurisdiction || "United Kingdom",
      Model: run?.model || app?.model || "unknown",
      Scope: "Selected answer",
      Generated: new Date().toLocaleString(),
      Citations: sources.length,
      Sources: fallbackSources.length,
    },
    sections: [
      { title: "Question", body: run?.question || "" },
      { title: "Answer", body: run?.answer || "" },
    ],
    sources: fallbackSources,
    footerNote: "Generated by ADA Studio. Preserve source verification and legal review before relying on this output.",
  };
}

function matterBundleExportPayload(app, bundleData, currentRun, includeAnswer = true) {
  const documents = bundleData?.documents || [];
  const sections = [
    {
      title: "Matter Overview",
      body: [
        `App: ${app?.name || bundleData?.app?.name || "Unknown app"}`,
        `Jurisdiction: ${app?.jurisdiction || bundleData?.app?.jurisdiction || "United Kingdom"}`,
        `Documents: ${documents.length}`,
        `Generated: ${new Date(bundleData?.generated_at || Date.now()).toLocaleString()}`,
      ].join("\n"),
    },
    {
      title: "Document Inventory",
      body: documents.length
        ? documents.map((doc, index) => `${index + 1}. ${doc.filename} - ${doc.text_extraction_status || "UNKNOWN"} - ${doc.chunk_count || 0} chunks`).join("\n")
        : "No documents are currently attached to this app.",
    },
  ];
  if (includeAnswer && currentRun?.answer) {
    sections.push(
      { title: "Selected Answer", body: `Question: ${currentRun.question}\n\n${currentRun.answer}` },
      { title: "Answer Citations", body: (currentRun.citations || []).map((citation) => `[${citation.tag}] ${citation.doc_name}\n${citation.preview}`).join("\n\n") || "No citations were attached to the selected answer." },
    );
  }
  sections.push({
    title: "Source Extract Appendix",
    body: documents.map((doc) => [
      `## ${doc.filename}`,
      `Status: ${doc.text_extraction_status || "UNKNOWN"}`,
      doc.index_truncated ? "Note: Indexed preview was truncated for Worker limits." : "",
      doc.text_preview || doc.text_extraction_message || "No text preview available.",
    ].filter(Boolean).join("\n")).join("\n\n"),
  });
  return {
    title: `${app?.name || bundleData?.app?.name || "ADA Studio"} - Matter Bundle`,
    subtitle: "Full app bundle with document inventory, selected answer, citations, and source appendix",
    metadata: {
      App: app?.name || bundleData?.app?.name || "Unknown app",
      Jurisdiction: app?.jurisdiction || bundleData?.app?.jurisdiction || "United Kingdom",
      Model: app?.model || bundleData?.app?.model || "unknown",
      Scope: includeAnswer && currentRun?.answer ? "Full matter bundle plus selected answer" : "Full matter bundle",
      Generated: new Date(bundleData?.generated_at || Date.now()).toLocaleString(),
      Documents: documents.length,
      Citations: currentRun?.citations?.length || 0,
    },
    sections,
    sources: [
      ...citationSources(currentRun),
      ...documentSources(documents, true),
    ],
    footerNote: "This bundle is an export of indexed app material and generated answers. Verify against the original uploaded documents before filing or sending.",
  };
}

